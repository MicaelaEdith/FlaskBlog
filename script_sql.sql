CREATE TABLE IF NOT EXISTS POST (
    id INT AUTO_INCREMENT PRIMARY KEY,
    id_user INT NOT NULL,
    post TEXT NOT NULL,
    url_img VARCHAR(255),
    title VARCHAR(255) NOT NULL,
    subtitle VARCHAR(255) NULL,
    FOREIGN KEY (id_user) REFERENCES USERS(id)
);
